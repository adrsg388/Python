operation=input("Enter the operation you want (+ - * /):  ")
num1=int(input("Enter your First Number: "))
num2=int(input("Enter your Second Number: "))

if operation=='+':
    print(num1+num2)
elif operation=='-':
    print(num1-num2)
elif operation=='*':
    print(num1*num2)
elif operation=='/':
    print(num1/num2)
else:
    print("operation is invalid")



 

